	}

	/**
	 * Remove the specified resource from storage.
	 *
	 * @param  int  $id
	 * @return Response
	 */
	public function destroy($id)
	{
		$brand = Brand::find($id);
		Prod::where('brand_id', '=', $id)->delete();
		$brand->delete();
